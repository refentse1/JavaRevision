package org.example.DataStructures.Queue;

public class LinkedList {
    /**
     * <h1>Linked List</h1>
     * <p>LinkedList class is almost identical to the ArrayList.</p>
     * <p>It has all the same methods as the ArrayList class because it implements the List interface just like the ArrayList</p>
     * <p>
     *     <strong>LinkedList stores items in containers. The list to the first container and each container has a link to the next container in the list. <br>
     * To add an element to the list, the element is placed into a new container and that container is linked to one of the other containers in the list.</strong>
     * </p>
     * <p>
     *     <strong>
     *         Use an ArrayList for storing and accessing data and LinkedList to manipulate data.
     *     </strong>
     * </p>
     * <h3>For most cases ArrayList is more efficient, as it is common to need to access random items</h3>
     * <ul>
     *     <li>
     *         <strong>addFirst():</strong>Adds an item to the beginning of the list.
     *     </li>
     *     <li>
     *         <strong>addLast():</strong>Add an item to the end of the list.
     *     </li>
     *     <li>
     *         <strong>removeFirst():</strong>Remove an item from the beginning of the list.
     *     </li>
     *     <li>
     *         <strong>removeLast():</strong>Remove an item from the end of the list.
     *     </li>
     *     <li>
     *         <strong>getFirst():</strong>Get the item at the beginning of the list.
     *     </li>
     *     <li>
     *         <strong>getLast():</strong>Get the item at the end of the list.
     *     </li>
     * </ul>
     * <br>
     * <p>
     *     <strong>To use it we create an object of the LinkedList class, and pass a wrapper class to the Generic Type.</strong>
     * </p>
     * <br>
     * @Example:
     * <br>
     * <code>
     *     LinkedList<`String`> stringLinkedList = new LinkedList<`String`>();
     * </code>
     */
    public LinkedList(){}
}

package org.example.DataStructures;

public class HashMap {
    /**
     * <h1>HashMap</h1>
     * <p>Hashmap is a collection type that stores items in Key/Value pairs like python dictionaries.</p>
     * <p>
     *     <strong>One object is used as a key(index) and another object (value). It can store different types. Meaning the key and value data types can be different</strong>
     * </p>
     * <p>
     *     <small>
     *         <strong>It uses generics for the datatype, meaning we use Wrapper classes for the types.</strong>
     *     </small>
     * </p>
     * <h3>HashMap Methods:</h3>
     * <ul>
     *     <li>
     *         <strong>put();</strong>it is used to add items to the collection. It takes in  the key and value as arguments, respectively.
     *     </li>
     *     <li>
     *         <strong>get();</strong>it is used to retrieve the value paired to the key that we pass as argument to the method.
     *     </li>
     *     <li>
     *         <strong>remove();</strong>it is used to remove an item from the collection, we pass the key to this item as an argument.
     *     </li>
     *     <li>
     *         <strong>clear();</strong>it is used to remove all items from the list.
     *     </li>
     *     <li>
     *         <strong>size();</strong>returns the count of the items in the collection
     *     </li>
     *     <li>
     *         <strong>keySet();</strong>this retrieves the keys in the collection.
     *     </li>
     *     <li>
     *         <strong>values();</strong>this retrieves the values in the collection.
     *     </li>
     * </ul>
     * <br>
     * @Example:
     * <br>
     * <code>
     *     HashMap<`TypeForKeys,TypeForValues`> hashMapVariableName = new HashMap<`TypeForKeys,TypeForValues`>()
     * </code>
     */
    public HashMap(){}
}

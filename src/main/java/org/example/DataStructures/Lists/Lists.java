    /**
     * <h1>List</h1>
     * <p>The list interface extends the Collection interface</p>
     * <p><small>
     *     The collection interface includes methods such as the add(),size(),remove(),iterator(),addAll(),removeAll(),clear()
     * </small></p>
     * <p>Since list is an interface we cannot create objects directly from it, however we can use the classes:</p>
     * <ul>
     *     <li>Arraylist</li>
     *     <li>LinkedList</li>
     *     <li>Vector</li>
     *     <li>Stack</li>
     * </ul>
     * <br>
     * <h3>Methods of List:</h3>
     * <ul>
     *     <li>
     *         <strong>add()</strong>, adds an element to a list.
     *     </li>
     *     <li>
     *         <strong>getAll()</strong>, adds all elements of one list to another.
     *     </li>
     *     <li>
     *         <strong>get()</strong>, helps to randomly access elements from lists
     *     </li>
     *     <li>
     *         <strong>iterator()</strong>, returns iterator object that can be used to sequentially access elements of lists.
     *     </li>
     *     <li>
     *         <strong>set()</strong>, changes the value of an element in a list.
     *     </li>
     *     <li>
     *         <strong>remove()</strong>, removes an element from the list.
     *     </li>
     *     <li>
     *         <strong>removeAll()</strong>, removes all elements from the list.
     *     </li>
     *     <li>
     *         <strong>clear()</strong>, removes all the elements from the list(more efficient than removeAll()).
     *     </li>
     *     <li>
     *         <strong>size()</strong>, returns the length of lists.
     *     </li>
     *     <li>
     *         <strong>toArray()</strong>, converts a list into an array.
     *     </li>
     *     <li>
     *         <strong>contains()</strong>, returns true if a list contains a specified element.
     *     </li>
     * </ul>
     * <br>
     * <h4>List vs Set</h4>
     * <p>List can include duplicate elements, whereas a set cannot have duplicate elements.</p>
     * <p>Elements in lists are stored in some order,however sets are stored in groups like sets in maths.</p>
     *
     */
